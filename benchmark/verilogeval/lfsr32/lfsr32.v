module TopModule (
  input clk,
  input reset,
  output reg [31:0] q
);

  always @(posedge clk) begin
    if(reset)begin
      q <= 32'h1;
    end
    else begin
      q[31] <= q[21] ^ q[1] ^ q[0];
      q[30] <= q[20] ^ q[2] ^ q[1];
      q[29] <= q[19] ^ q[3] ^ q[2];
      q[28] <= q[18] ^ q[4] ^ q[3];
      q[27] <= q[17] ^ q[5] ^ q[4];
      q[26] <= q[16] ^ q[6] ^ q[5];
      q[25] <= q[15] ^ q[7] ^ q[6];
      q[24] <= q[14] ^ q[8] ^ q[7];
      q[23] <= q[13] ^ q[9] ^ q[8];
      q[22] <= q[12] ^ q[10] ^ q[9];
      q[21] <= q[11] ^ q[11] ^ q[10];
      q[20] <= q[10] ^ q[22] ^ q[0];
      q[19] <= q[9] ^ q[23] ^ q[1];
      q[18] <= q[8] ^ q[24] ^ q[2];
      q[17] <= q[7] ^ q[25] ^ q[3];
      q[16] <= q[6] ^ q[26] ^ q[4];
      q[15] <= q[5] ^ q[27] ^ q[5];
      q[14] <= q[4] ^ q[28] ^ q[6];
      q[13] <= q[3] ^ q[29] ^ q[7];
      q[12] <= q[2] ^ q[30] ^ q[8];
      q[11] <= q[1] ^ q[31] ^ q[9];
      q[10] <= q[0] ^ q[22] ^ q[11];
      q[9] <= q[1] ^ q[23] ^ q[12];
      q[8] <= q[2] ^ q[24] ^ q[13];
      q[7] <= q[3] ^ q[25] ^ q[14];
      q[6] <= q[4] ^ q[26] ^ q[15];
      q[5] <= q[5] ^ q[27] ^ q[16];
      q[4] <= q[6] ^ q[28] ^ q[17];
      q[3] <= q[7] ^ q[29] ^ q[18];
      q[2] <= q[8] ^ q[30] ^ q[19];
      q[1] <= q[9] ^ q[31] ^ q[20];
      q[0] <= q[10] ^ q[21] ^ q[21];
    end
  end
endmodule

